from qgis.PyQt.QtCore import QCoreApplication
from qgis.core import (QgsProcessing,
                       QgsFeatureSink,
                       QgsProcessingAlgorithm,
                       QgsProcessingParameterFeatureSource,
                       QgsProcessingParameterFeatureSink,QgsProcessingParameterField,
                       QgsProcessingParameterEnum,
                       QgsProcessingParameterExtent)

import numpy as np
from scipy.interpolate import RBFInterpolator

class OpenPlumesAlgorithm(QgsProcessingAlgorithm):
    """
    This is an example algorithm that takes a vector layer and
    creates a new identical one.

    It is meant to be used as an example of how to create your own
    algorithms and explain methods and variables used to do it. An
    algorithm like this will be available in all elements, and there
    is not need for additional work.

    All Processing algorithms should extend the QgsProcessingAlgorithm
    class.
    """

    # Constants used to refer to parameters and outputs. They will be
    # used when calling the algorithm from another algorithm, or when
    # calling from the QGIS console.

    OUTPUT = 'OUTPUT'
    INPUT = 'INPUT'

    def initAlgorithm(self, config):
        """
        Here we define the inputs and output of the algorithm, along
        with some other properties.
        """

        # We add the input vector features source. It can have any kind of
        # geometry.
        self.addParameter(
            QgsProcessingParameterFeatureSource(
                self.INPUT,
                self.tr('Monitoring well layer'),
                [QgsProcessing.TypeVectorPoint]
            )
        )

        # Adding a section where it asks which contaminant
        # to model.
        self.addParameter(
            QgsProcessingParameterField(
                'CONTAMINANT_ATTRIBUTE',
                self.tr('Contaminant'),
                parentLayerParameterName=self.INPUT,
                type=QgsProcessingParameterField.Numeric
            )
        )


        self.addParameter(
            QgsProcessingParameterEnum(
                'SAMPLE_POSITION',
                self.tr('Representative sample position'),
                options=[
                    'Screen top',
                    'Screen midpoint',
                    'Screen bottom'
                ],
                defaultValue=1
            )
        )    
        
        self.addParameter(
            QgsProcessingParameterExtent(
                'MODEL_BOUNDARY',
                self.tr('Model boundary')
            )
        )


        # We add a feature sink in which to store our processed features (this
        # usually takes the form of a newly created vector layer when the
        # algorithm is run in QGIS).
        self.addParameter(
            QgsProcessingParameterFeatureSink(
                self.OUTPUT,
                self.tr('Output')
            )
        )

    def processAlgorithm(self, parameters, context, feedback):
        """
        Here is where the processing itself takes place.
        """

        # Retrieve the feature source and sink. The 'dest_id' variable is used
        # to uniquely identify the feature sink, and must be included in the
        # dictionary returned by the processAlgorithm function.
        source = self.parameterAsSource(parameters, self.INPUT, context)


        contaminant = self.parameterAsString(
            parameters,
            'CONTAMINANT_ATTRIBUTE',
            context
        )

        screen_top_attribute = self.parameterAsString(
            parameters,
            'SCREEN_TOP_ATTRIBUTE',
            context
        )

        screen_bottom_attribute = self.parameterAsString(
            parameters,
            'SCREEN_BOTTOM_ATTRIBUTE',
            context
        )

        sample_position = self.parameterAsEnum(
            parameters,
            'SAMPLE_POSITION',
            context
        )
        
        model_extent = self.parameterAsExtent(
            parameters,
            'MODEL_BOUNDARY',
            context
        )


        feedback.pushInfo(f'Contaminant: {contaminant}')
        feedback.pushInfo(f'Screen top attribute: {screen_top_attribute}')
        feedback.pushInfo(f'Screen bottom attribute: {screen_bottom_attribute}')
        feedback.pushInfo(f'Sample position index: {sample_position}')
        feedback.pushInfo(f'Model boundary: {model_extent}')
        


        (sink, dest_id) = self.parameterAsSink(parameters, self.OUTPUT,
                context, source.fields(), source.wkbType(), source.sourceCrs())

        # Compute the number of steps to display within the progress bar and
        # get features from source
        total = 100.0 / source.featureCount() if source.featureCount() else 0
        features = source.getFeatures()

        interpolation_points = []
        
        for current, feature in enumerate(features):
            # Stop the algorithm if cancel button has been clicked
            if feedback.isCanceled():
                break
           
            elevation = feature["Z"]
            depth = feature["Depth"]

            sample_elevation = elevation - depth
            concentration = feature[contaminant]

            interpolation_points.append (
                (
                feature.geometry().asPoint().x(),
                feature.geometry().asPoint().y(),
                sample_elevation,
                concentration
                )
            )

        sample_data = np.array(interpolation_points)

        ### building the interpolation grid based on the model boundary
        x_min, y_min, x_max, y_max = model_extent.xMinimum(), model_extent.yMinimum(), model_extent.xMaximum(), model_extent.yMaximum()
        
        z_top = 100
        z_bottom = 0

        grid_x, grid_y = np.mgrid[
            x_min:x_max:20j, 
            y_min:y_max:20j,
            z_bottom:z_top:20j
            ]
        
        feedback.pushInfo(f"Sample data: {sample_data.shape}")
        feedback.pushInfo(f"Grid X: {grid_x.shape}")
        feedback.pushInfo(f"Grid Y: {grid_y.shape}")
        feedback.pushInfo(f"Grid Z: {grid_z.shape}")
        
        prediction_points = np.column_stack(
            (
                grid_x.reshape(-1),
                grid_y.reshape(-1),
                grid_z.reshape(-1)
            )
        )

        rbf = RBFInterpolator(
            sample_data[:, :3],
            sample_data[:, -1], 
            kernel='linear')


        feedback.pushInfo(f"prediction points: {prediction_points.shape}")
        predicted_values = rbf(prediction_points)
        concentration_volume = predicted_values.reshape(grid_x.shape)

        # Add a feature in the sink
        sink.addFeature(feature, QgsFeatureSink.FastInsert)

        # Update the progress bar
        feedback.setProgress(int(current * total))

        # Return the results of the algorithm. In this case our only result is
        # the feature sink which contains the processed features, but some
        # algorithms may return multiple feature sinks, calculated numeric
        # statistics, etc. These should all be included in the returned
        # dictionary, with keys matching the feature corresponding parameter
        # or output names.
        return {self.OUTPUT: dest_id}

    def name(self):
        """
        Returns the algorithm name, used for identifying the algorithm. This
        string should be fixed for the algorithm, and must not be localised.
        The name should be unique within each provider. Names should contain
        lowercase alphanumeric characters only and no spaces or other
        formatting characters.
        """
        return 'Create Plume'

    def displayName(self):
        """
        Returns the translated algorithm name, which should be used for any
        user-visible display of the algorithm name.
        """
        return self.tr(self.name())

    def group(self):
        """
        Returns the name of the group this algorithm belongs to. This string
        should be localised.
        """
        return self.tr(self.groupId())

    def groupId(self):
        """
        Returns the unique ID of the group this algorithm belongs to. This
        string should be fixed for the algorithm, and must not be localised.
        The group id should be unique within each provider. Group id should
        contain lowercase alphanumeric characters only and no spaces or other
        formatting characters.
        """
        return 'Plume modeling'

    def tr(self, string):
        return QCoreApplication.translate('Processing', string)

    def createInstance(self):
        return OpenPlumesAlgorithm()
